class Doante {
  int idColaborador;
  String nombre;
  String apellido;
  String correo;
  String telefono;
  String direccion;
  Doante(
    this.idColaborador,
    this.nombre,
    this.apellido,
    this.correo,
    this.telefono,
    this.direccion,
  ) {
    idColaborador = idColaborador;
    nombre = nombre;
    apellido = apellido;
    correo = correo;
    telefono = telefono;
    direccion = direccion;
  }
}
