import 'dart:ui';

class Equipos {
  int idEquipo;
  String nombreEquipo;
  String procesador;
  String memoriaRAM;
  String discoDuro;
  Image imagen;

  Equipos(
    this.idEquipo,
    this.nombreEquipo,
    this.procesador,
    this.memoriaRAM,
    this.discoDuro,
    this.imagen,
  ) {
    idEquipo = idEquipo;
    nombreEquipo = nombreEquipo;
    procesador = procesador;
    memoriaRAM = memoriaRAM;
    discoDuro = discoDuro;
    imagen = imagen;
  }
}
